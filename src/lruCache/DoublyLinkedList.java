package lruCache;

public class DoublyLinkedList {
	
	private Node head;
	private Node tail;
	public DoublyLinkedList() {
		
	}
	public Node getHead() {
		return head;
	}
	public void setHead(Node head) {
		this.head = head;
	}
	public Node getTail() {
		return tail;
	}
	public void setTail(Node tail) {
		this.tail = tail;
	}
	
	

}
